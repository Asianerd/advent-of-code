class Cumbers:
    max_length = [0, 0]
    collections = [[], []]  # [>, v]
    directional_increments = {
        0: [1, 0],
        1: [0, 1]
    }

    def __init__(self, position):
        self.position = position

    def update(self, direction):
        # 0 = >
        # 1 = v
        target_position = [
            self.position[0] + Cumbers.directional_increments[direction][0],
            self.position[1] + Cumbers.directional_increments[direction][1]
        ]
        target_position = Cumbers.correct_position(target_position)
        target = Cumbers.fetch_at(target_position)
        if not target:
            self.position = target_position

    @staticmethod
    def static_update():
        for direction, collection in enumerate(Cumbers.collections):
            for item in collection:
                item.update(direction)

    @staticmethod
    def static_init():
        Cumbers.max_length = [
            len(raw_data_string.strip().split("\n")[0]),
            len(raw_data_string.strip().split("\n"))
        ]
        for y, column in enumerate(raw_data_string.strip().split("\n")):
            for x, item in enumerate(column):
                if item == '.':
                    continue

                if item == '>':
                    Cumbers.collections[0].append(Cumbers([x, y]))
                else:
                    Cumbers.collections[1].append(Cumbers([x, y]))

    @staticmethod
    def fetch_at(position):
        for collection in Cumbers.collections:
            for item in collection:
                if item.position == position:
                    return item
        return None

    @staticmethod
    def correct_position(position):
        target_position = list(position)
        for index in range(2):
            if position[index] > Cumbers.max_length[index]:
                target_position[index] = position[index] - Cumbers.max_length[index] - 1
            elif position[index] < 0:
                target_position[index] = position[index] + Cumbers.max_length[index] + 1
        return target_position


