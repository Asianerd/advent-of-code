class Line:
    class Character(Enum):
        paren = 0
        bracket = 1
        curly = 2
        angle = 3

    class CharacterType(Enum):
        opening = 0
        closing = 1

    class Status(Enum):
        completed = 0
        corrupted = 1
        incomplete = 2

    lines = []

    character_enum_match = {
        Character.paren: "()",
        Character.bracket: "[]",
        Character.curly: "{}",
        Character.angle: "<>"
    }

    def __init__(self, line_string):
        self.line_string = line_string
        self.line_data = []
        for x in line_string:
            self.line_data.append(Line.identify_character(x))
        self.valid = True
        self.status = Line.Status.completed

    def update_status(self):
        pass

    @staticmethod
    def identify_character(string):
        for key in Line.character_enum_match:
            if string in Line.character_enum_match[key]:
                return key


