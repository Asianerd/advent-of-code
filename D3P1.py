position = [0, 0]
increment = [3, 1]
finished = False


def wrap_around(pos):
    global finished
    final = pos
    while pos[0] >= map_size[0]:
        final[0] -= map_size[0]
    while pos[1] >= map_size[1]:
        final[1] -= map_size[1]
        finished = True
    return final


collisions = 0
while not finished:
    position[0] += increment[0]
    position[1] += increment[1]

    position = wrap_around(position)

    if finished:
        break

    collided = False
    if field[position[1]][position[0]] == "#":
        collisions += 1
        collided = True
    print(f"{position} : {collided}")


print(collisions)
